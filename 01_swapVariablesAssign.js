console.log("Swap Three Variables Values");
var num1 = 100; // 200
var num2 = 200; // 100
var num3 = 300; //300
console.log("======== Before Swap ==========");
console.log("num1:", num1, " num2:", num2, "num3:", num3);

console.log("======== After Swap ==========");
var temp = num1;
num1 = num2;
num2 = num3;
num3 = temp;
console.log("num1:", num1, " num2:", num2," num3:", num3);


